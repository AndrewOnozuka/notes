x_center_str = float(input("Enter coordinate x_center:"))
y_center_str = float(input("Enter coordinate y_center:"))
x_p1_str = float(input("Enter coordinate x_p1:"))
y_p1_str = float(input("Enter coordinate y_p1:"))
x_p2_str = float(input("Enter coordinate x_p2:"))
y_p2_str = float(input("Enter coordinate y_p2:"))
pi = float(3.14)
area_circle1 = pi * ((((y_p1_str) - (y_center_str)) ** 2) + (((x_p1_str) - (x_center_str)) ** 2))
area_circle2 = pi * ((((y_p2_str) - (y_center_str)) ** 2) + (((x_p2_str) - (x_center_str)) ** 2))
area_ring = max(area_circle1,area_circle2) - min(area_circle1,area_circle2)
print("Area of ring shape =", round(area_ring,2))